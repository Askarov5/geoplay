/// Get the list of neighbors for a country.
List<String> getNeighbors(String code) {
  return adjacencyGraph[code] ?? [];
}

/// Check if two countries share a land border.
bool isNeighbor(String code1, String code2) {
  return getNeighbors(code1).contains(code2);
}

/// Check if a country has land borders.
bool hasLandBorders(String code) {
  return adjacencyGraph.containsKey(code);
}
